package jp.eightbit.exam.Model;

public class RankingModel {

	private int rank;
	
	private String nameAve;
	
	private String ave;

	private String nameFast;
	
	private int fastest;

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public String getNameAve() {
		return nameAve;
	}

	public void setNameAve(String nameAve) {
		this.nameAve = nameAve;
	}

	public String getAve() {
		return ave;
	}

	public void setAve(String ave) {
		this.ave = ave;
	}

	public String getNameFast() {
		return nameFast;
	}

	public void setNameFast(String nameFast) {
		this.nameFast = nameFast;
	}

	public int getFastest() {
		return fastest;
	}

	public void setFastest(int fastest) {
		this.fastest = fastest;
	}

}
